package com.example.b07demosummer2024.model;

/**
 * Encapsulates a web address.
 */
public class Resource {

    private String name;
    private String url;

    /**
     * Gets the name.
     */
    public String getName() { return name; }

    /**
     * Gets the url.
     */
    public String getUrl()  { return url; }
}
